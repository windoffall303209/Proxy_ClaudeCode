-- Visual search: multimodal image embeddings (e.g. NVIDIA NV-CLIP) for product primary images
-- PostgreSQL version.

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TABLE IF NOT EXISTS product_image_embeddings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id INTEGER NOT NULL UNIQUE REFERENCES products(id) ON DELETE CASCADE,
    product_image_id INTEGER NULL REFERENCES product_images(id) ON DELETE SET NULL,
    image_url VARCHAR(2048) NOT NULL,
    content_hash CHAR(64) NOT NULL,
    embedding_model VARCHAR(128) NOT NULL,
    embedding_dim INTEGER NOT NULL DEFAULT 512,
    embedding_vector TEXT NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_content_hash ON product_image_embeddings(content_hash);

DROP TRIGGER IF EXISTS set_updated_at_product_image_embeddings ON product_image_embeddings;
CREATE TRIGGER set_updated_at_product_image_embeddings
    BEFORE UPDATE ON product_image_embeddings
    FOR EACH ROW
    EXECUTE FUNCTION set_updated_at();
